def obtener_key(diccionario, autor):
    if autor in diccionario:
        return list(diccionario[autor].keys())
    else:
        return "Artista No encontrado"
    
def obtenerCancion(diccionario,autor,cancion):
    if autor in diccionario and cancion in diccionario[autor]:
        return diccionario[autor][cancion]
    else:
        return "No existe esa cancion en la biblioteca"




